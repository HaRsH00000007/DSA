public class A_Arithmetic_Operators_Binary_Type{
    public static void main (String args[]){
        int A=10;
        int B=5;
        // (1.) Binary Operators
        System.out.println("Sum of A & B is = "+(A+B));

        System.out.println("Multiple of A & B is = " + (A * B));

        System.out.println("Subtraction of A & B is = "+ (A - B ));

        System.out.println("Modulo (Remainder) of A & B is ="+( A % B));

        System.out.println("Divide of A & B is = "+( A / B));

    }
}
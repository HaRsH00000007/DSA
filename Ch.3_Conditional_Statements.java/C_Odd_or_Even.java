import java.util.*;
public class C_Odd_or_Even{
    public static void main(String args[]){
        Scanner sc = new Scanner(System.in);
        int num = sc.nextInt();
        if (num%2==0){
            System.out.println("The given num is Even");
        }

        else{
            System.out.println("The given num is Odd");
        }

    }
}
